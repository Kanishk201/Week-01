public class SamsAverage {
    public static void main(String[] args) {
	
	   //Marks in each subject
	   int maths = 94;
	   int physics = 95;
	   int chemistry = 96;
	   
	   //Calculate average
	   double averagePercent = (maths+physics+chemistry) / 3.0;
	   
	   //Output
	   System.out.println("Sam's average mark in PCM is " + averagePercent);
	}
}